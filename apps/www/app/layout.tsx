import { MainNav } from "@/components/main-nav";
import { ThemeProvider } from "@/components/ui/theme-provider";
import { TooltipProvider } from "@/components/ui/tooltip";
import type { Metadata } from "next";
import localFont from "next/font/local";
import { getDocs, groupBySlug } from "./docs/utils";
import "./globals.css";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

const switzerFont = localFont({
  src: [
    {
      path: "./assets/Switzer-Variable.ttf",
    },
    {
      path: "./assets/Switzer-VariableItalic.ttf",
    },
  ],
  display: "swap",
  weight: "100..900",
});

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const docs = await getDocs();

  return (
    <html lang="en" suppressHydrationWarning className={switzerFont.className}>
      <body>
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          disableTransitionOnChange
        >
          <TooltipProvider>
            <div className="m-auto px-8 pt-[70px] xl:max-w-[1200px]">
              <MainNav config={groupBySlug(docs)} />
              {children}
            </div>
          </TooltipProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
